package logic;

/**
 * The `ETypeProduct` enumeration represents the different types of products that can be categorized.
 */
public enum ETypeProduct {
    /**
     * Represents products related to groceries or food items.
     */
    VIVERES,

    /**
     * Represents alcoholic beverages or liquors.
     */
    LICORES,

    /**
     * Represents medical or pharmaceutical products.
     */
    MEDICINAS,

    /**
     * Represents products related to household cleaning and hygiene.
     */
    ASEO
}
